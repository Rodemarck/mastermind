CREATE TABLE usuarios(
  id INT PRIMARY KEY AUTO_INCREMENT,
  login VARCHAR(30) UNIQUE NOT NULL,
  senha VARCHAR(80) NOT NULL
);

CREATE TABLE jogo(
  id INT PRIMARY KEY AUTO_INCREMENT,
  id_criador INT NOT NULL,
  dataCriacao VARCHAR(24) NOT NULL,
  respostas VARCHAR(4) NOT NULL,
  FOREIGN KEY (id_criador) REFERENCES usuarios(id)
);

CREATE TABLE tabuleiro(
  id INT PRIMARY KEY AUTO_INCREMENT,
  matriz VARCHAR(40) DEFAULT '0000000000000000000000000000000000000000',
  pedras VARCHAR(40) DEFAULT '0000000000000000000000000000000000000000',
  indice TINYINT NOT NULL,
  id_jogo INT NOT NULL,
  id_jogador INT NOT NULL,
  FOREIGN KEY (id_jogo) REFERENCES jogo(id),
  FOREIGN KEY (id_jogador) REFERENCES usuarios(id)
);